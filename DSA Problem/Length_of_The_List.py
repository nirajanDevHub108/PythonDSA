def length_of_list(arr):
    """
    Function to calculate the length of a list without using built-in functions.
    
    Parameters:
    arr (list): The list whose length is to be calculated.
    
    Returns:
    int: The length of the list.
    """
  
    return len(arr)

arr = [54, 43, 2, 1, 5]

count = length_of_list(arr)  # Call the function to get the length of the list
print(count)  # Get the length of the list